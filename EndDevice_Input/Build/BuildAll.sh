#!/bin/sh

BUILDMODEL="BLUE"
#BUILDMODEL="RED"

if [ ${BUILDMODEL} = "BLUE" ]; then
	make TWE_CHIP_MODEL=JN5164 clean
	make TWE_CHIP_MODEL=JN5164 all -j 4
	make TWE_CHIP_MODEL=JN5164 BOARD=LITE2525A clean
	make TWE_CHIP_MODEL=JN5164 BOARD=LITE2525A all -j 4
	make TWE_CHIP_MODEL=JN5164 BOARD=SWING clean
	make TWE_CHIP_MODEL=JN5164 BOARD=SWING all -j 4
	make TWE_CHIP_MODEL=JN5164 BOARD=OTA clean
	make TWE_CHIP_MODEL=JN5164 BOARD=OTA all -j 4
elif [ ${BUILDMODEL} = "RED" ]; then
	make TWELITE=RED clean
	make TWELITE=RED all
	make TTWELITE=RED BOARD=OTA clean
	make TWELITE=RED BOARD=OTA all
else
	echo "Error"
fi
