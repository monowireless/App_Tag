#!/bin/sh

BUILDMODEL="BLUE"
#BUILDMODEL="RED"

if [ ${BUILDMODEL} = "BLUE" ]; then
	make TWE_CHIP_MODEL=JN5164 clean
	make TWE_CHIP_MODEL=JN5164 all -j 4
	make TWE_CHIP_MODEL=JN5164 TWE_BOARD_MODEL=MONOSTICK clean
	make TWE_CHIP_MODEL=JN5164 TWE_BOARD_MODEL=MONOSTICK all -j 4
elif [ ${BUILDMODEL} = "RED" ]; then
	make TWELITE=RED clean
	make TWELITE=RED all
	make TWELITE=RED TWE_BOARD_MODEL=MONOSTICK clean
	make TWELITE=RED TWE_BOARD_MODEL=MONOSTICK all -j 4
else
	echo "Error"
fi
